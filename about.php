This year JOTA-JOTI 2020 will take place from 16 to 18 October. The dynamic programme features a variety of non-formal education activities, including webinars, global campfires, talent shows, live shows, and much more. World Scouting’s global partners are actively involved in the event, offering their expertise and fun activities to engage participants. The JOTA-JOTI programme aims to support Scouts and non-Scouts of all ages to learn about communications technology, the values of global citizenship, and how together we can solve the most pressing challenges facing our planet.

Keeping young people Safe from Harm online is an important aspect of this event. Through chat moderation, “listening ears”, e-learning courses and a number of sessions about how to be safe online, we make sure that all participants have a positive and inclusive JOTA-JOTI experience.

JOTA-JOTI is one of World Scouting’s longest-running global events dating back more than 60 years to 1958. The digital educational event has seen steady annual growth in participation over the years and with a focus on the future aims to reach millions of more participants in the years to come.</p>
